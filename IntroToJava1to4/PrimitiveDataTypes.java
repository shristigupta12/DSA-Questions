package IntroToJava1to4;

public class PrimitiveDataTypes {
    // Primitive data types are the ones which we cannot further break and classify into other data types.
    int num = 34;
    float decimal = 456.3f;
    char letter = 'a';
    double longDecimal = 4235265241354526.54196517464;
    long longInteger = 9915344544444444L;
    boolean check = true;
}
